                            <p style="text-align: justify; text-indent: 5%">
                                Universitas Jambi (UNJA) envisions becoming a "World Class Entrepreneurship University"
                                based on Agroindustry and the Environment, as outlined in its Strategic Plan (Renstra). This
                                vision places an outward focus on the development of UNJA SMART in the future. One of the
                                indicators for achieving this vision includes: (1) Increase in faculty's scholarly
                                publications in reputable international journals. (2) Enhancement of research collaborations
                                with national and international institutions. (3) Improvement in student achievements to
                                enhance their insights. In the effort to realize this vision, these activities serve as a
                                platform for Universitas Jambi to build collaborations and networks to achieve several
                                strategic goals. The development and strengthening of partnerships are being pursued to
                                support the Chemistry Study Program's path to international accreditation, particularly from
                                the Royal Society of Chemistry (RSC). The implementation's impact is reflected in the
                                preparedness of the study program to produce graduates who can adapt, contribute, and
                                compete globally. In the context of 21st-century skills, which emphasize integrated
                                abilities in knowledge, skills, and attitudes, the Chemistry Study Program is responsible
                                for fostering skills such as critical thinking, problem-solving, communication, creativity,
                                and collaboration. Additionally, the study program aims to provide students with ample
                                opportunities to develop themselves not only within the campus but also in the business and
                                industrial world (DUDI), with the goal of enhancing their competencies beyond the
                                curriculum.
                            </p>
                            <p style="text-align: justify; text-indent: 5%">
                                This program aligns with UNJA's strategic goal of becoming a World Class Entrepreneurship
                                University. Through this program, UNJA aims to connect and engage in mutually beneficial
                                collaborations with renowned higher education institutions. These activities also open
                                opportunities for both domestic and international partnerships in the realms of education,
                                research, and community engagement. Furthermore, this initiative serves as a platform for
                                Universitas Jambi to organize Group Discussion Forums related to the International
                                Accreditation of the Chemistry Study Program and best practices for Independent Learning and
                                Campus Freedom, facilitating students' rights to engage in activities beyond the campus. The
                                Chemistry Study Program is encouraged to implement the Independent Learning and Campus
                                Freedom (MBKM) Policy established by the Ministry of Education and Culture. This can be
                                achieved through cooperation agreements (MoUs/SPKs) with partners, including state
                                universities, government agencies, and DUDIs, encompassing activities such as education,
                                internships in the business and industrial sectors, research, and community service like
                                Community Service Learning (KKN).
