                                According to the list of internationally recognized accreditation agencies and
                                certifications endorsed by the Ministry of Education, Culture, Research, and Technology, the
                                Royal Society of Chemistry (RSC) is among the recognized institutions. Strengthening
                                international accreditation aims to cultivate exceptional human resources, playing a crucial
                                role in the transformation of higher education. This, in turn, contributes to the
                                development of resilient individuals capable of thinking and acting extraordinarily,
                                adapting rapidly to challenges, remaining competitive, and succeeding in the competitive
                                landscape. The impact of this initiative extends to other study programs within the scope of
                                Universitas Jambi.
